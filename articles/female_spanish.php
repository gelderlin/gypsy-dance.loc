<p align="justify">
Испанские костюмы, представленные в этой галерее, вы можете использовать не только как костюмы для фламенко, но и как карнавальные костюмы испанки и цыганки.
</p>
<p align="justify">
Объём юбок не превышает полутора солнца, к тому же кокетка, присутствующая на некоторых костюмах, помешает активному использованию юбок в сценическом танце русских цыган. Но зато они очень пригодятся в танцах, где преобладают проходки, чечётки и работа руками. Лёгкость юбок поможет непринуждённому исполнению танцевального номера. 
</p>
<p align="justify">
Несмотря на то, что размах юбки недостаточен для некоторых па, его вполне хватит для того, чтобы изобразить танцующую гадалку на корпоративном празднике. А значит, эти платья вполне могут стать карнавальным костюмом цыганочки. В конце концов, можно перевоплотиться несколько раз за вечер, по-разному надевая костюм и используя разлчиные аксессуары (шаль, платки, цветы, бубны и т.п.)
</p>
